<h1>About WikiSearch</h1>
<p>This is a part of our <a href="https://github.com/Raysmond/EnWikiIndexing">EnWikiIndexing</a> project for Distributed
    System course 2013 in
    <a href="http://www.fudan.edu.cn" target="_blank">Fudan University</a>. EnWikiIndexing is a simple program to create
    full inverted indexes
    for English Wikipedia dump in XML format.
</p>
<p>This is the demo web page for searching articles through inverted indexes built by EnWikiIndexing.</p>

